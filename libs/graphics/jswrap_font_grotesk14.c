JsVar *jswrap_graphics_setFontGrotesk14(JsVar *parent, int scale) {
  if (scale<1) scale=1;
  JsVar *bitmap = jsvNewNativeString(fontBitmap, sizeof(fontBitmap));
  JsVar *widths = jsvNewNativeString(fontWidths, sizeof(fontWidths));
  JsVar *r = jswrap_graphics_setFontCustom(parent, bitmap, 32, widths, 14 + (scale<<8) + (2<<16)); // 2 bit
  jsvUnLock2(bitmap, widths);
  return r;
}
