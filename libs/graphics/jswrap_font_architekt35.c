JsVar *jswrap_graphics_setFontArchitekt35(JsVar *parent, int scale) {
  if (scale<1) scale=1;
  JsVar *bitmap = jsvNewNativeString(fontBitmap, sizeof(fontBitmap));
  JsVar *widths = jsvNewFromInteger(20);
  JsVar *r = jswrap_graphics_setFontCustom(parent, bitmap, 32, widths, 35 + (scale<<8) + (2<<16)); // 2 bit
  jsvUnLock2(bitmap, widths);
  return r;
}
